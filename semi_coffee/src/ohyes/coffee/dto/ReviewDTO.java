package ohyes.coffee.dto;
/*
		이름               널?       유형             
---------------- -------- -------------- 
REVIEW_NO        NOT NULL NUMBER         
REVIEW_P_NO      NOT NULL VARCHAR2(6)    
REVIEW_TITLE     NOT NULL VARCHAR2(100)  
REVIEW_MEMID     NOT NULL VARCHAR2(12)   
REVIEW_CONTENT   NOT NULL VARCHAR2(4000) 
REVIEW_DATE      NOT NULL DATE           
REVIEW_READCOUNT NOT NULL NUMBER         
REVIEW_STATUS             NUMBER(1) 
 */

public class ReviewDTO {
	
	private int reviewNo;
	private String reviewPNo;
	private String reviewTitle;
	private String reviewMemid;
	private String reviewContent;
	private String reviewDate;
	private int reviewReadcount;
	private int reviewStatus;
	
	public ReviewDTO() {
		// TODO Auto-generated constructor stub
	}

	public int getReviewNo() {
		return reviewNo;
	}

	public void setReviewNo(int reviewNo) {
		this.reviewNo = reviewNo;
	}

	public String getReviewPNo() {
		return reviewPNo;
	}

	public void setReviewPNo(String reviewPNo) {
		this.reviewPNo = reviewPNo;
	}

	public String getReviewTitle() {
		return reviewTitle;
	}

	public void setReviewTitle(String reviewTitle) {
		this.reviewTitle = reviewTitle;
	}

	public String getReviewMemid() {
		return reviewMemid;
	}

	public void setReviewMemid(String reviewMemid) {
		this.reviewMemid = reviewMemid;
	}

	public String getReviewContent() {
		return reviewContent;
	}

	public void setReviewContent(String reviewContent) {
		this.reviewContent = reviewContent;
	}

	public String getReviewDate() {
		return reviewDate;
	}

	public void setReviewDate(String reviewDate) {
		this.reviewDate = reviewDate;
	}

	public int getReviewReadcount() {
		return reviewReadcount;
	}

	public void setReviewReadcount(int reviewReadcount) {
		this.reviewReadcount = reviewReadcount;
	}

	public int getReviewStatus() {
		return reviewStatus;
	}

	public void setReviewStatus(int reviewStatus) {
		this.reviewStatus = reviewStatus;
	}
	
	
}
